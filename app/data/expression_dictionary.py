# 역방향 딕셔너리 (내부용)
_REVERSE: dict[str, str] = {
    synonym: representative
    for representative, synonyms in EXPRESSION_DICTIONARY.items()
    for synonym in synonyms
}

def get_representative(token: str) -> str:
    """
    토큰을 대표형으로 변환.
    사전에 없으면 원형 그대로 반환 (정보 손실 없음).
    """
    return _REVERSE.get(token, token)